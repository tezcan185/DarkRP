    description = [[The Admin Police is only used by admins, they do the most basic admin things.]],
    weapons = {"Glock", "StunStick", "Arrest Baton", "Unarrest Baton"},
    command = "adminpolice",
    max = 3,
    salary = 50,
    admin = 1,
    vote = false,
    hasLicense = true,
    NeedToChangeFrom = TEAM_POLICE
})
